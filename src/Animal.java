public class Animal {
 private String name;
 private int age;
 private int speed;
 private String weight;

 public Animal(String name, int age, int speed, String weight) {
  this.name = name;
  this.age = age;
  this.speed = speed;
  this.weight = weight;
 }

 public String getName() {
  return name;
 }

 public void setName(String name) {
  this.name = name;
 }

 public int getAge() {
  return age;
 }

 public void setAge(int age) {
  this.age = age;
 }

 public int getSpeed() {
  return speed;
 }

 public void setSpeed(int speed) {
  this.speed = speed;
 }

 public String getWeight() {
  return weight;
 }

 public void setWeight(String weight) {
  this.weight = weight;
 }
}

